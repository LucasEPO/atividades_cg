package atividade03;

/**
 *  Classe para as coordenadas normais vn de um .obj
 *
 * @author lucas
 */
public class Normal {
    double x, y, z;

    /**
     * Cria um objeto com tres parametros x, y e z para coordenadas de normais em um .obj
     *
     * @param x
     * @param y
     * @param z
     */
    public Normal(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
